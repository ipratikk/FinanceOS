import Foundation

public enum AppShadow {
    // Shadow radius and color for different elevation levels
    public static let subtleRadius: CGFloat = 2
    public static let cardRadius: CGFloat = 12
    public static let floatRadius: CGFloat = 24
    public static let modalRadius: CGFloat = 48

    public static let subtleOpacity: Double = 0.20
    public static let cardOpacity: Double = 0.40
    public static let floatOpacity: Double = 0.60
    public static let modalOpacity: Double = 0.80
}
